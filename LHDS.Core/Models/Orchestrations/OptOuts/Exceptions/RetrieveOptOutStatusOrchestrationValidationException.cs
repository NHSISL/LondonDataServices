using Xeptions;

namespace LHDS.Core.Models.Orchestrations.OptOuts.Exceptions
{
    public class RetrieveOptOutStatusOrchestrationValidationException : Xeption
    {
        private const string validationMessage = "Retrieve OptOut Status orchestration validation errors occurred, please try again.";

        public RetrieveOptOutStatusOrchestrationValidationException(Xeption innerException, string validationSummary = "")
            : base(
                  message: validationSummary.Length > 0
                    ? $"{validationMessage}  Validation errors: {validationSummary}"
                    : validationMessage,
                  innerException)
        { }
    }
}